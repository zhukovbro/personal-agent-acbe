import sys

def check_ranges():
    """–ü—Ä–æ–≤–µ—Ä—è–µ—Ç —Ç—Ä–æ–π–∫–∏ —á–∏—Å–µ–ª –Ω–∞ –ø–æ–ø–∞–¥–∞–Ω–∏–µ –≤ –¥–∏–∞–ø–∞–∑–æ–Ω"""
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –∞—Ä–≥—É–º–µ–Ω—Ç—ã –∫–æ–º–∞–Ω–¥–Ω–æ–π —Å—Ç—Ä–æ–∫–∏
    args = sys.argv[1:]  # —É–±–∏—Ä–∞–µ–º –∏–º—è —Å–∫—Ä–∏–ø—Ç–∞
    
    if len(args) == 0:
        print("‚ùå –û—à–∏–±–∫–∞: –Ω—É–∂–Ω–æ —É–∫–∞–∑–∞—Ç—å —á–∏—Å–ª–∞ –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏")
        print("üìñ –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ: python3 check_range.py 75 70 80 120 110 130")
        print("üìñ –§–æ—Ä–º–∞—Ç: –∑–Ω–∞—á–µ–Ω–∏–µ –º–∏–Ω –º–∞–∫—Å –∑–Ω–∞—á–µ–Ω–∏–µ –º–∏–Ω –º–∞–∫—Å ...")
        return
    
    if len(args) % 3 != 0:
        print("‚ùå –û—à–∏–±–∫–∞: –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —á–∏—Å–µ–ª –¥–æ–ª–∂–Ω–æ –±—ã—Ç—å –∫—Ä–∞—Ç–Ω–æ 3")
        print("üìñ –ö–∞–∂–¥–∞—è —Ç—Ä–æ–π–∫–∞: –∑–Ω–∞—á–µ–Ω–∏–µ –º–∏–Ω–∏–º—É–º –º–∞–∫—Å–∏–º—É–º")
        return
    
    # –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º —Ç—Ä–æ–π–∫–∏ —á–∏—Å–µ–ª
    print("üîç –ü—Ä–æ–≤–µ—Ä–∫–∞ –¥–∏–∞–ø–∞–∑–æ–Ω–æ–≤:")
    print("=" * 50)
    
    try:
        for i in range(0, len(args), 3):
            value = float(args[i])
            min_val = float(args[i + 1])
            max_val = float(args[i + 2])
            
            # –ü—Ä–æ–≤–µ—Ä—è–µ–º –¥–∏–∞–ø–∞–∑–æ–Ω
            is_in_range = min_val <= value <= max_val
            status = "‚úÖ –í –¥–∏–∞–ø–∞–∑–æ–Ω–µ" if is_in_range else "‚ùå –í–Ω–µ –¥–∏–∞–ø–∞–∑–æ–Ω–∞"
            
            print(f"{value:8.1f} | [{min_val:6.1f} - {max_val:6.1f}] | {status}")
            
    except ValueError:
        print("‚ùå –û—à–∏–±–∫–∞: –≤—Å–µ –∞—Ä–≥—É–º–µ–Ω—Ç—ã –¥–æ–ª–∂–Ω—ã –±—ã—Ç—å —á–∏—Å–ª–∞–º–∏")
        return
    
    print("=" * 50)

if __name__ == "__main__":
    check_ranges() 